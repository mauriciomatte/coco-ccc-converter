// Para um charCode ASCII, devolve { idx, inverse }: idx = indice no MC6847_FONT; inverse = true para
// minusculas (o VDG nao tem glifos minusculos -> mostra a MAIUSCULA em video inverso).
export function mc6847Glyph(code: number): { idx: number; inverse: boolean } {
  if (code >= 0x61 && code <= 0x7a) return { idx: code - 0x60, inverse: true }; // a-z -> A-Z inverso
  if (code >= 0x40 && code <= 0x5f) return { idx: code - 0x40, inverse: false }; // @ A-Z [ \ ] ^ _
  if (code >= 0x20 && code <= 0x3f) return { idx: code, inverse: false };        // espaco ! ... ?
  return { idx: 0x20, inverse: false }; // fallback: espaco
}
